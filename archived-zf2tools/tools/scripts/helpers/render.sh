#!/bin/bash

#############################################################################
# Render Text
#############################################################################
function RenderText {
	echo -e $1
}

#############################################################################
# Render a Thick Divider Line
#############################################################################
function RenderLineThick () {
	printf "%78s\n" | tr ' ' =
}

#############################################################################
# Render a Thin Divider Line
#############################################################################
function RenderLineThin {
	printf "%78s\n" | tr ' ' -
}
